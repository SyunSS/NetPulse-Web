const RootProviders = defineComponent({
  setup() {
    const { isDark } = useDark()
    return () =>
      h(NConfigProvider, {
        theme: isDark.value ? darkTheme : null,
        themeOverrides: isDark.value ? notionDarkOverrides : notionLightOverrides,
        locale: zhCN,
        dateLocale: dateZhCN
      }, {
        default: () =>
          h(NMessageProvider, null, {
            default: () =>
              h(NDialogProvider, null, {
                default: () => h(App),
              }),
          }),
      })
  },
})

const app = createApp(RootProviders)
const pinia = createPinia()
app.use(pinia)
app.use(router)
app.mount('#app')
